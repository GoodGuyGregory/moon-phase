
function setup() {
    createCanvas(windowWidth, windowHeight);

}

// waxing gibbouse
function waxingGibbousMoon() {

    fill(0);
    stroke(255);
    strokeWeight(5);
    fill(255)
    ellipse(windowWidth / 2, windowHeight / 2, 450, 450);
}


function draw() {
    background(15);

    //    development for moon phase here
    waxingGibbousMoon();


}
